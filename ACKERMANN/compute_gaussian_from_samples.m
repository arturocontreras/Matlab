function [mu, P] = compute_gaussian_from_samples(x)

%--------------------------------------------------------------------------
% 1. CONFIGURACION INICIAL
%--------------------------------------------------------------------------
%  1.1. Numero de muestras
N = size(x,1);

%  1.2. Dimension de las muestras
D = size(x,2);

%--------------------------------------------------------------------------
% 2. CALCULO DE LA MEDIA
%--------------------------------------------------------------------------
mu = mean (x,1);

%--------------------------------------------------------------------------
% 3. CALCULO DE LA COVARIANZA
%--------------------------------------------------------------------------
%  3.1. TERMINO USUAL
M = x - ones(N,1)*mu;

%  3.2. CALCULO DE LA COVARIANZA
P = zeros(D);
for n = 1:N
    P = P + M(n,:)'*M(n,:);
end
P = (1/N)*P;

end